// +| ExportHTML |+
// 輸出HTML
//////////////////////////////////////////////////////////////////////////////
public static function ExportHTML ( $argv , $Content , $Options            ) {
  ////////////////////////////////////////////////////////////////////////////
  $TAG            = ""                                                       ;
  $PARAMETERS     = ""                                                       ;
  $NOTAIL         = 0                                                        ;
  ////////////////////////////////////////////////////////////////////////////
  $C              = array         (                                        ) ;
  $K              = array_keys    ( $argv                                  ) ;
  ////////////////////////////////////////////////////////////////////////////
  foreach                         ( $K as $L                               ) {
    $S            = strtolower    ( $L                                     ) ;
    switch                        ( $S                                     ) {
      case "function"                                                        :
      break                                                                  ;
      case "tag"                                                             :
        $TAG      = $argv [ $L ]                                             ;
      break                                                                  ;
      case "notail"                                                          :
        $V        = $argv [ $L ]                                             ;
        if                        ( strlen ( $V ) <= 0                     ) {
          $NOTAIL = 1                                                        ;
        } else                                                               {
          $NOTAIL = $V                                                       ;
        }                                                                    ;
      break                                                                  ;
      default                                                                :
        $V        = $argv [ $L ]                                             ;
        $P        = "{$L}=\"{$V}\""                                          ;
        array_push                ( $C , $P                                ) ;
      break                                                                  ;
    }                                                                        ;
  }                                                                          ;
  ////////////////////////////////////////////////////////////////////////////
  if                              ( count ( $C ) > 0                       ) {
    $PARAMETERS   = implode       ( " " , $C                               ) ;
  }                                                                          ;
  ////////////////////////////////////////////////////////////////////////////
  if                              ( strlen ( $TAG ) <= 0                   ) {
    return ""                                                                ;
  }                                                                          ;
  ////////////////////////////////////////////////////////////////////////////
  if                              ( $NOTAIL > 0                            ) {
    return "<{$TAG} {$PARAMETERS} />"                                        ;
  }                                                                          ;
  ////////////////////////////////////////////////////////////////////////////
  return   "<{$TAG} {$PARAMETERS}>{$Content}</{$TAG}>"                       ;
  ////////////////////////////////////////////////////////////////////////////
}
