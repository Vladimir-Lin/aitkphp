                $INP -> AddPair         ( "type"     , "text"              ) ;
                $INP -> AddPair         ( "class"  , "NameItemAppellation" ) ;
                $INP -> AddPair         ( "onchange" , $JSC                ) ;
                $INP -> AddPair         ( "value"    , $NAME               ) ;
              }                                                              ;
              ////////////////////////////////////////////////////////////////
            } else                                                           {
              ////////////////////////////////////////////////////////////////
              $TDX = $TRX -> addTd    ( $NAME                              ) ;
              $TDX -> AddPair         ( "class"  , "NameItemAppellation"   ) ;
              ////////////////////////////////////////////////////////////////
            }                                                                ;
            //////////////////////////////////////////////////////////////////
          }                                                                  ;
        }                                                                    ;
      } else                                                                 {
        $TDX   = $TRX  -> addTd       (                                    ) ;
        $TDX  -> addSpan              ( $rr [ $K ] , "" , "NameItemColumn" ) ;
      }                                                                      ;
      ////////////////////////////////////////////////////////////////////////
      $IdAt    = $IdAt + 1                                                   ;
      ////////////////////////////////////////////////////////////////////////
    }                                                                        ;
    //////////////////////////////////////////////////////////////////////////
  }                                                                          ;
  ////////////////////////////////////////////////////////////////////////////
  if                                  ( count ( $TITLEs ) > 0              ) {
    //////////////////////////////////////////////////////////////////////////
    $TITx      = array                (                                    ) ;
    $IdAt      = 0                                                           ;
    foreach                           ( $TITLEs as $T                      ) {
      ////////////////////////////////////////////////////////////////////////
      if                              ( $AT == $IdAt                       ) {
        foreach                       ( $TRANSLATEs as $L                  ) {
          if                          ( array_key_exists ( $L , $LANGs )   ) {
            array_push                ( $TITx , $LANGs [ $L ]              ) ;
          }                                                                  ;
        }                                                                    ;
      } else                                                                 {
        array_push                    ( $TITx , $T                         ) ;
      }                                                                      ;
      ////////////////////////////////////////////////////////////////////////
      $IdAt    = $IdAt + 1                                                   ;
      ////////////////////////////////////////////////////////////////////////
    }                                                                        ;
    //////////////////////////////////////////////////////////////////////////
    $TRX       = $THREAD -> addTr     (                                    ) ;
    foreach                           ( $TITx as $T                        ) {
      $TD      = $TRX    -> addTd     ( $T                                 ) ;
      $TD     -> AddPair              ( "class" , "SqlTableHeader"         ) ;
    }                                                                        ;
    //////////////////////////////////////////////////////////////////////////
    $TFOOT     = $HT     -> addHtml   ( "tfoot"                            ) ;
    //////////////////////////////////////////////////////////////////////////
    $TRX       = $TFOOT  -> addTr     (                                    ) ;
    foreach                           ( $TITx as $T                        ) {
      $TD      = $TRX    -> addTd     ( $T                                 ) ;
      $TD     -> AddPair              ( "class" , "SqlTableHeader"         ) ;
    }                                                                        ;
    //////////////////////////////////////////////////////////////////////////
  }                                                                          ;
  ////////////////////////////////////////////////////////////////////////////
  $DBX  -> Close                      (                                    ) ;
  ////////////////////////////////////////////////////////////////////////////
  return $HT -> Content               (                                    ) ;
}
