          $EDIT    = false                                                   ;
        }                                                                    ;
      break                                                                  ;
      default                                                                :
        if                            ( ! in_array ( $K , $PARAMETERs )    ) {
          $V       = self::GetTag     ( $K    , $argv                      ) ;
          $PARAMS [ $K ] = $V                                                ;
        }                                                                    ;
      break                                                                  ;
