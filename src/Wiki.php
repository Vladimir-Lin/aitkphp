                                         $DELETE                           ) {
  ////////////////////////////////////////////////////////////////////////////
  $canAppend    = strtolower           ( $APPEND                           ) ;
  $DISPAPPEND   = ""                                                         ;
  if ( in_array ( $canAppend , [ "yes" , "true" ] ) )                        {
    $canAppend  = true                                                       ;
  } else                                                                     {
    $canAppend  = false                                                      ;
    $DISPAPPEND = "display: none;"                                           ;
  }                                                                          ;
  ////////////////////////////////////////////////////////////////////////////
  $canDelete    = strtolower           ( $DELETE                           ) ;
  if ( in_array ( $canDelete , [ "yes" , "true" ] ) )                        {
    $canDelete  = true                                                       ;
  } else                                                                     {
    $canDelete  = false                                                      ;
  }                                                                          ;
  ////////////////////////////////////////////////////////////////////////////
  $ACCOUNTS     = file_get_contents    ( $FILE                             ) ;
  $ACCTS        = explode              ( "\n" , $ACCOUNTS                  ) ;
