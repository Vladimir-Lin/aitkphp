    if ( in_array ( $MinStr , [ "no" , "false" ] ) )                         {
      $DoMinify = false                                                      ;
    }                                                                        ;
  }                                                                          ;
  ////////////////////////////////////////////////////////////////////////////
  $DIRS         = $Options        [ "Directory"                            ] ;
  ////////////////////////////////////////////////////////////////////////////
  $DIR          = self::GetTag    ( "directory" , $argv                    ) ;
