  foreach                              ( $ACCTS as $act                    ) {
    if                                 ( strlen ( $act ) > 0               ) {
      $LL       = explode              ( ":" , $act                        ) ;
      if                               ( count ( $LL ) > 1                 ) {
        array_push                     ( $LISTS , $LL [ 0 ]                ) ;
      }                                                                      ;
    }                                                                        ;
  }                                                                          ;
  ////////////////////////////////////////////////////////////////////////////
  $LINES        = array                (                                   ) ;
  if                                   ( count ( $LISTS ) > 0              ) {
