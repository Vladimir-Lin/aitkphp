      $PATHX    = $DIRS           [ $DIR                                   ] ;
      $FILE     = "{$PATHX}/{$FILENAME}"                                     ;
      $TEXT     = file_get_contents ( $FILE                                ) ;
      if                          ( ! $TEXT                                ) {
      } else                                                                 {
        $Content = $TEXT                                                     ;
      }                                                                      ;
    }                                                                        ;
  }                                                                          ;
  ////////////////////////////////////////////////////////////////////////////
  if                              ( strlen ( $Content ) > 0                ) {
    if                            ( $DoMinify                              ) {
      ////////////////////////////////////////////////////////////////////////
      $DOMX     = new Minify\JS   (                                        ) ;
      $DOMX    -> add             ( $Content                               ) ;
      $MSG      = $DOMX -> minify (                                        ) ;
      ////////////////////////////////////////////////////////////////////////
    } else                                                                   {
      $MSG      = $Content                                                   ;
    }                                                                        ;
  } else                                                                     {
    $MSG        = ""                                                         ;
  }                                                                          ;
  ////////////////////////////////////////////////////////////////////////////
  return "<script type='text/javascript'>{$MSG}</script>"                    ;
