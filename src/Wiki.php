// +| CiosAJAX |+
// CIOS AJAX進入點
//////////////////////////////////////////////////////////////////////////////
public static function CiosAJAX      ( $DB , $PICKDB , $TABLE , $HH , $AA  ) {
  ////////////////////////////////////////////////////////////////////////////
  $METHOD      = $HH -> Parameter    ( "Method"                            ) ;
  $ID          = $HH -> Parameter    ( "Id"                                ) ;
  $COLUMN      = $HH -> Parameter    ( "Column"                            ) ;
  $VALUE       = $HH -> Parameter    ( "Value"                             ) ;
  ////////////////////////////////////////////////////////////////////////////
  switch                             ( $METHOD                             ) {
    case "Update"                                                            :
      $DB     -> LockWrites          ( [ $TABLE                          ] ) ;
      $QQ      = "update {$TABLE} set `{$COLUMN}` = ?"                       .
                 " where ( `id` = {$ID} ) ;"                                 ;
      $qq      = $DB -> Prepare      ( $QQ                                 ) ;
      $qq     -> bind_param          ( 's' , $VALUE                        ) ;
      $qq     -> execute             (                                     ) ;
      $DB     -> UnlockTables        (                                     ) ;
    break                                                                    ;
  }                                                                          ;
  ////////////////////////////////////////////////////////////////////////////
  $AA [ "Answer" ] = "Yes"                                                   ;
  ////////////////////////////////////////////////////////////////////////////
  return $AA                                                                 ;
}
