                                          $PATH                              ,
                                          $EXTS                              ,
                                          $EXTENSION                         ,
                                          $EDITABLE                          ,
                                          $TEMPL                             ,
                                          $ROOT                            ) {
  ////////////////////////////////////////////////////////////////////////////
  if                                    ( ! $EDITABLE                      ) {
    return ""                                                                ;
  }                                                                          ;
  ////////////////////////////////////////////////////////////////////////////
  $XENTRY    = $TEMPL                                                        ;
  ////////////////////////////////////////////////////////////////////////////
  if ( ( strlen ( $PATH ) <= 0 ) or ( $PATH == "/" ) )                       {
    $DIRPATH = ""                                                            ;
