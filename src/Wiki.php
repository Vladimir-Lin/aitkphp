public static function GetFunction ( $KEY , $argv , $Maps                  ) {
  ////////////////////////////////////////////////////////////////////////////
  $func = self::GetTag             ( $KEY , $argv                          ) ;
  if                               ( strlen ( $func ) <= 0                 ) {
    return ""                                                                ;
  }                                                                          ;
  $func = strtolower               ( $func                                 ) ;
  ////////////////////////////////////////////////////////////////////////////
  if                               ( ! array_key_exists ( $func , $Maps )  ) {
    return ""                                                                ;
  }                                                                          ;
  ////////////////////////////////////////////////////////////////////////////
  return $Maps [ $func ]                                                     ;
}
