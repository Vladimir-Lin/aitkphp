    $NAMX     -> Locality  = $LANG                                           ;
    $NAMX     -> Priority  = -1                                              ;
    $NAMX     -> Relevance = $RELV                                           ;
    $NAMX     -> Flags     = 0                                               ;
    $NAMX     -> Name      = $NAME                                           ;
    $DB       -> LockWrites            ( [ $TABLE ]                        ) ;
    $NAMX     -> Editing               ( $DB , $TABLE                      ) ;
    $DB       -> UnlockTables          (                                   ) ;
    //////////////////////////////////////////////////////////////////////////
    $RELOAD    = true                                                        ;
    //////////////////////////////////////////////////////////////////////////
  } else                                                                     {
    //////////////////////////////////////////////////////////////////////////
    $ORDER     = $HH -> Parameter      ( "Order"                           ) ;
    $COLUMN    = $HH -> Parameter      ( "Column"                          ) ;
    $VALUE     = $HH -> Parameter      ( "Value"                           ) ;
    //////////////////////////////////////////////////////////////////////////
    $NAMX     -> Id  = $ORDER                                                ;
    $NAMX     -> ObtainsById           ( $DB , $TABLE                      ) ;
    //////////////////////////////////////////////////////////////////////////
    switch                             ( $COLUMN                           ) {
      case "language"                                                        :
        $NAMX -> Locality  = $VALUE                                          ;
        if ( $NAMX -> isExistsId ( $DB , $TABLE ) )                          {
          $NAMX -> Priority = $NAMX -> LastPosition ( $DB , $TABLE )         ;
          $RELOAD = true                                                     ;
        }                                                                    ;
        $DB   -> LockWrites            ( [ $TABLE ]                        ) ;
        $DB   -> SyncId                ( $DB , $TABLE                      ) ;
        $DB   -> UnlockTables          (                                   ) ;
      break                                                                  ;
      case "relevance"                                                       :
        $NAMX -> Relevance = $VALUE                                          ;
        if ( $NAMX -> isExistsId ( $DB , $TABLE ) )                          {
          $NAMX -> Priority = $NAMX -> LastPosition ( $DB , $TABLE )         ;
          $RELOAD = true                                                     ;
        }                                                                    ;
        $DB   -> LockWrites            ( [ $TABLE ]                        ) ;
        $DB   -> SyncId                ( $DB , $TABLE                      ) ;
        $DB   -> UnlockTables          (                                   ) ;
      break                                                                  ;
      case "priority"                                                        :
        $NAMX -> Priority  = $VALUE                                          ;
        if ( $NAMX -> isExistsId ( $DB , $TABLE ) )                          {
          $NAMX -> Priority = $NAMX -> LastPosition ( $DB , $TABLE )         ;
          $RELOAD = true                                                     ;
        }                                                                    ;
        $DB   -> LockWrites            ( [ $TABLE ]                        ) ;
        $DB   -> SyncId                ( $DB , $TABLE                      ) ;
        $DB   -> UnlockTables          (                                   ) ;
      break                                                                  ;
      case "flags"                                                           :
        $NAMX -> Flags = intval        ( $VALUE , 16                       ) ;
        $DB   -> LockWrites            ( [ $TABLE ]                        ) ;
        $NAMX -> UpdateFlagsById       ( $DB , $TABLE                      ) ;
        $DB   -> UnlockTables          (                                   ) ;
      break                                                                  ;
      case "name"                                                            :
        //////////////////////////////////////////////////////////////////////
        $NAMX -> Name = $VALUE                                               ;
        //////////////////////////////////////////////////////////////////////
        $DB   -> LockWrites            ( [ $TABLE ]                        ) ;
        $NAMX -> Editing               ( $DB , $TABLE                      ) ;
        $DB   -> UnlockTables          (                                   ) ;
        //////////////////////////////////////////////////////////////////////
        if                             ( strlen ( $VALUE ) <= 0            ) {
          $RELOAD = true                                                     ;
        }                                                                    ;
        //////////////////////////////////////////////////////////////////////
      break                                                                  ;
    }                                                                        ;
    //////////////////////////////////////////////////////////////////////////
  }                                                                          ;
  ////////////////////////////////////////////////////////////////////////////
  if                                   ( $RELOAD                           ) {
    //////////////////////////////////////////////////////////////////////////
    $MSG       = self::CreateUuidNames ( $DB                                 ,
                                         $PICKDB                             ,
                                         $TABLE                              ,
                                         $ID                                 ,
                                         $UUID                               ,
                                         $LANGUAGE                           ,
                                         $LOCALITY                           ,
                                         true                                ,
                                         $Options                          ) ;
    //////////////////////////////////////////////////////////////////////////
  }                                                                          ;
  ////////////////////////////////////////////////////////////////////////////
  $AA [ "Message" ] = $MSG                                                   ;
  $AA [ "Answer"  ] = "Yes"                                                  ;
  ////////////////////////////////////////////////////////////////////////////
  return $AA                                                                 ;
}
