// 產生亂數
//////////////////////////////////////////////////////////////////////////////
public static function RandomString ( $PREFIX , $LENGTH                    ) {
  ////////////////////////////////////////////////////////////////////////////
  $L      = "0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz" ;
  $S      = $PREFIX                                                          ;
  ////////////////////////////////////////////////////////////////////////////
  while                             ( strlen ( $S ) < $LENGTH              ) {
    //////////////////////////////////////////////////////////////////////////
    $I    = rand                    ( 0  , 61                              ) ;
    $T    = substr                  ( $L , $I , 1                          ) ;
    $S    = "{$S}{$T}"                                                       ;
    //////////////////////////////////////////////////////////////////////////
  }                                                                          ;
  ////////////////////////////////////////////////////////////////////////////
  return $S                                                                  ;
}
