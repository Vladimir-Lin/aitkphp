// -| ListFolderItems |-
//////////////////////////////////////////////////////////////////////////////
// +| GrepFolderItems |+
// 列出檔案項目
//////////////////////////////////////////////////////////////////////////////
public static function GrepFolderItems    ( $CURRENT , $EXTS               ) {
  ////////////////////////////////////////////////////////////////////////////
  $FILES        = self::ListFolderItems   ( $CURRENT                       ) ;
  $EXT          = explode                 ( " " , $EXTS                    ) ;
  ////////////////////////////////////////////////////////////////////////////
  $SUBDIRS      = array                   (                                ) ;
  $FILEX        = array                   (                                ) ;
  $ITEMS        = array                   (                                ) ;
  ////////////////////////////////////////////////////////////////////////////
  foreach                                 ( $FILES as $F                   ) {
    $FILE       = "{$CURRENT}/{$F}"                                          ;
    if                                    ( ( $F == "."                  )  or
                                            ( $F == ".."                 ) ) {
    } else
    if                                    ( is_dir ( $FILE )               ) {
      array_push                          ( $SUBDIRS , $F                  ) ;
    }                                                                        ;
  }                                                                          ;
  ////////////////////////////////////////////////////////////////////////////
  $CUX          = $CURRENT                                                   ;
  $CUX          = str_replace             ( "\\" , "/" , $CUX              ) ;
  $CUX          = str_replace             ( "//" , "/" , $CUX              ) ;
  ////////////////////////////////////////////////////////////////////////////
  foreach                                 ( $EXT as $E                     ) {
    foreach                               ( glob ("{$CUX}/{$E}") as $F     ) {
      $X        = str_replace             ( "\\"      , "/" , $F           ) ;
      $X        = str_replace             ( "//"      , "/" , $X           ) ;
      $X        = str_replace             ( "{$CUX}/" , ""  , $X           ) ;
      $X        = str_replace             ( $CUX      , ""  , $X           ) ;
      if                                  ( ! in_array ( $X , $SUBDIRS )   ) {
        if                                ( ! in_array ( $X , $FILEX   )   ) {
          array_push                      ( $FILEX , $X                    ) ;
        }                                                                    ;
      }                                                                      ;
    }                                                                        ;
  }                                                                          ;
  ////////////////////////////////////////////////////////////////////////////
  foreach                                 ( $SUBDIRS as $F                 ) {
    array_push                            ( $ITEMS    , $F                 ) ;
  }                                                                          ;
  ////////////////////////////////////////////////////////////////////////////
  foreach                                 ( $FILEX   as $F                 ) {
    array_push                            ( $ITEMS    , $F                 ) ;
  }                                                                          ;
  ////////////////////////////////////////////////////////////////////////////
  return $ITEMS                                                              ;
}
