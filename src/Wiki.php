    $CURRENT = dirname                  ( dirname ( $CURRENT )             ) ;
    $CURRENT = str_replace              ( "\\"  , "/" , $CURRENT           ) ;
    $CURRENT = str_replace              ( $ROOT , ""  , $CURRENT           ) ;
    $CURRENT = str_replace              ( "//"  , "/" , $CURRENT           ) ;
