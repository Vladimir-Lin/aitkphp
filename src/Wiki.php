    $XICON   = "{$EXTENSION}/images/24x24/catalog.png"                       ;
  } else                                                                     {
    $DIRPATH = $DIRECTORY                                                    ;
    $CURRENT = "{$ROOT}{$PATH}/.."                                           ;
