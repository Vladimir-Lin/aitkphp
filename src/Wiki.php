  }                                                                          ;
  ////////////////////////////////////////////////////////////////////////////
  $FTYPE     = "目錄" ;
  $FILEXT    = ""                                                            ;
  $FILESIZE  = ""                                                            ;
  $FILEDT    = ""                                                            ;
  ////////////////////////////////////////////////////////////////////////////
  $JSC   = "AitkChangeDirectory('{$ID}','{$BROWSABLE}','{$DOWNLOAD}','{$DIRPATH}','{$CURRENT}','{$EXTS}');" ;
  $ONCLICK = " onclick=\"{$JSC}\""                                           ;
