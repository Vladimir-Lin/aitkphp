        //////////////////////////////////////////////////////////////////////
      } else                                                                 {
        //////////////////////////////////////////////////////////////////////
        $TDX    -> AddText             ( $NAME                             ) ;
        //////////////////////////////////////////////////////////////////////
      }                                                                      ;
      ////////////////////////////////////////////////////////////////////////
    }                                                                        ;
  }                                                                          ;
  ////////////////////////////////////////////////////////////////////////////
  if                                   ( $EDIT                             ) {
    //////////////////////////////////////////////////////////////////////////
    $TRX         = $HT  -> addTr       (                                   ) ;
    //////////////////////////////////////////////////////////////////////////
    $KID         = "Name-Item-Language-{$UUID}"                              ;
    $TDX         = $TRX -> addTd       (                                   ) ;
    $TDX        -> AddPair             ( "width" , "1%"                    ) ;
    $TDX        -> AddPair             ( "class" , "NameColumnCell"        ) ;
    $SEL         = $TDX -> addSelect   (                                   ) ;
    $SEL        -> AddPair             ( "class"    , "NameColumnLocality" ) ;
    $SEL        -> AddPair             ( "id"    , $KID                    ) ;
    $SEL        -> addOptions          ( $LANGs  , $LANG                   ) ;
    //////////////////////////////////////////////////////////////////////////
    $KID         = "Name-Item-Relevance-{$UUID}"                             ;
    $TDX         = $TRX -> addTd       (                                   ) ;
    $TDX        -> AddPair             ( "width" , "1%"                    ) ;
