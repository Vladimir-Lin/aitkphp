public static function ComposeDirectory ( $ID                                ,
                                          $BROWSABLE                         ,
                                          $DOWNLOAD                          ,
                                          $DIRECTORY                         ,
                                          $PATH                              ,
                                          $EXTS                              ,
                                          $Options                         ) {
