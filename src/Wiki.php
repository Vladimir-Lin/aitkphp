public static function EarthVelocity ( $Distance )                           {
  ////////////////////////////////////////////////////////////////////////////
  $MU  = "132749350000000000000"                                             ;
  $AUS = "149597870700"                                                      ;
  $DAU = gmp_mul  ( $AUS , 2         )                                       ;
  $RAX = gmp_mul  ( $AUS , $Distance )                                       ;
  $DAU = gmp_sub  ( $DAU , $Distance )                                       ;
  $DAU = gmp_mul  ( $DAU , $MU       )                                       ;
  $DAU = gmp_div  ( $DAU , $RAX      )                                       ;
  ////////////////////////////////////////////////////////////////////////////
  return gmp_sqrt ( $DAU             )                                       ;
}
